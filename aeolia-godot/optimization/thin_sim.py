"""
Parameterized Python port of the Aeolia history engine (Phases 1–5).

Designed for black-box parameter optimization: fast, no Godot process required.
Consumes a ``base_world`` dict (pre-generated by run_headless.gd) that supplies
arch geography, plateau-edge adjacency, and substrate data. Replaces all
hardcoded magic numbers in history_engine.gd with entries from a ``params``
dict, so an optimizer can tune them freely.

Pipeline
--------
Phase 1  Dijkstra dual-source wavefront  (fixed edge costs — tunes DF timing)
Phase 2  Σ2ⁿ contact redistribution      (fixed fractions — tunes era shape)
Phase 3  Status assignment                (parameterized)
Phase 4  Population model (5 eras)        (fully parameterized)
Phase 5  Final state construction         (derived)

Usage
-----
    from thin_sim import simulate, DEFAULT_PARAMS

    with open("worlds/seed_42.json") as f:
        base_world = json.load(f)

    result = simulate(base_world, DEFAULT_PARAMS)
    # result["states"], result["df_year"], result["epi_log"], ...

For optimization, import ``params.py`` for bounds and suggest_params helpers.
"""

from __future__ import annotations

import heapq
import math
import random
from typing import Optional

# ---------------------------------------------------------------------------
# Crop-distance helper — mirrors history_engine._crop_distance exactly
# ---------------------------------------------------------------------------

_TROPICAL  = frozenset({"paddi", "taro", "sago"})
_TEMPERATE = frozenset({"emmer", "papa"})


def _crop_distance(contactor: str, contacted: str) -> float:
    if contactor == contacted:
        return 0.2
    ct = contactor in _TROPICAL
    cd = contacted  in _TROPICAL
    ce = contactor in _TEMPERATE
    de = contacted  in _TEMPERATE
    if (ct and cd) or (ce and de):
        return 0.5
    if frozenset([contactor, contacted]) == frozenset(["paddi", "papa"]):
        return 1.0
    return 0.8


def _log2(x: float) -> float:
    return math.log2(x) if x > 0 else 0.0


# ---------------------------------------------------------------------------
# Edge cost function — mirrors history_engine._edge_cost exactly.
# Kept fixed (not parameterised) so Phase-1 graph topology is stable.
# To tune Dark Forest timing, adjust edge-cost multipliers in params.py
# and pass them into _edge_cost via an edge_cost_scale param.
# ---------------------------------------------------------------------------

_ERA_BOUNDS = (-500, -200)


def _base_era_cost(year: int, hops: int, power: str) -> int:
    is_lattice  = power == "lattice"
    is_garrison = is_lattice and hops <= 3
    if year < -500:
        if is_garrison: return 167
        if is_lattice:  return 12000
        if hops <= 1:   return 350
        if hops <= 2:   return 580
        if hops <= 3:   return 1060
        return 8000
    elif year < -200:
        if is_garrison: return 85
        if is_lattice:  return 350 if hops <= 5 else 700
        if hops <= 4:   return 125
        if hops <= 6:   return 145
        return 200
    else:
        return 61


def _edge_cost(year: int, hops: int, power: str) -> int:
    cost = _base_era_cost(year, hops, power)
    for b in _ERA_BOUNDS:
        if year < b < year + cost:
            alt = (b - year + 1) + _base_era_cost(b + 1, hops, power)
            if alt < cost:
                return alt
    return cost


# ---------------------------------------------------------------------------
# Phase 1: Dijkstra dual-source wavefront
# ---------------------------------------------------------------------------

def _dijkstra(N: int, adj: list[list[int]],
              reach_arch: int, lattice_arch: int) -> tuple:
    """
    Returns (claimed, arrival_yr, hop_count, df_year, df_arch, df_detector).
    claimed[i] = "reach" | "lattice" | None
    arrival_yr[i] = int (year BP, negative) | None
    """
    R_START = -5500
    L_START = -5000

    claimed    = [None] * N
    arrival_yr = [None] * N
    hop_count  = [0]    * N

    claimed[reach_arch]   = "reach"
    arrival_yr[reach_arch] = R_START
    claimed[lattice_arch]  = "lattice"
    arrival_yr[lattice_arch] = L_START

    # heap: (year, arch_idx, power, hops, from_arch)
    pq: list = []
    for nb in adj[reach_arch]:
        c = _edge_cost(R_START, 1, "reach")
        heapq.heappush(pq, (R_START + c, nb, "reach", 1, reach_arch))
    for nb in adj[lattice_arch]:
        c = _edge_cost(L_START, 1, "lattice")
        heapq.heappush(pq, (L_START + c, nb, "lattice", 1, lattice_arch))

    df_year = df_arch = df_detector = None

    while pq:
        year, idx, power, hops, from_arch = heapq.heappop(pq)

        if claimed[idx] is not None:
            if claimed[idx] != power and df_year is None:
                df_year = year; df_arch = idx; df_detector = power
            continue

        claimed[idx]    = power
        arrival_yr[idx] = year
        hop_count[idx]  = hops

        for nb in adj[idx]:
            if claimed[nb] is not None and claimed[nb] != power and df_year is None:
                df_year = year; df_arch = idx; df_detector = power

        for nb in adj[idx]:
            if claimed[nb] is None:
                c = _edge_cost(year, hops + 1, power)
                heapq.heappush(pq, (year + c, nb, power, hops + 1, idx))

    return claimed, arrival_yr, hop_count, df_year, df_arch, df_detector


# ---------------------------------------------------------------------------
# Phase 2: Σ2ⁿ contact redistribution
# ---------------------------------------------------------------------------

def _redistribute(N: int, claimed: list, arrival_yr: list,
                  reach_arch: int, lattice_arch: int,
                  df_year: Optional[int],
                  adj: list[list[int]]) -> tuple:
    """
    Remaps Dijkstra arrival order onto four era time windows so that
    discovery accelerates geometrically across eras.

    Returns (claimed_new, arrival_yr_new, df_year_new, df_arch_new, df_detector_new).
    Fractions (0.05/0.10/0.20/0.40) are fixed — they ARE the Σ2ⁿ design target.
    """
    contactable = [
        i for i in range(N)
        if claimed[i] is not None and i not in (reach_arch, lattice_arch)
    ]
    # Stable sort: index tiebreaker matches JS Array.sort stable behaviour.
    contactable.sort(key=lambda i: (arrival_yr[i], i))
    nc = len(contactable)

    serial_n     = max(1, round(nc * 0.05))
    colonial_n   = max(1, round(nc * 0.10))
    industrial_n = max(2, round(nc * 0.20))
    nuclear_n    = max(2, round(nc * 0.40))
    total_slots  = serial_n + colonial_n + industrial_n + nuclear_n

    arrival_yr = arrival_yr[:]          # copy
    claimed    = claimed[:]

    for k, i in enumerate(contactable):
        if k < serial_n:
            arrival_yr[i] = -5000 + round((k + 1) / (serial_n + 1) * 3000)
        elif k < serial_n + colonial_n:
            j = k - serial_n
            arrival_yr[i] = -2000 + round((j + 1) / (colonial_n + 1) * 1500)
        elif k < serial_n + colonial_n + industrial_n:
            j = k - serial_n - colonial_n
            arrival_yr[i] = -500 + round((j + 1) / (industrial_n + 1) * 300)
        elif k < total_slots:
            j   = k - serial_n - colonial_n - industrial_n
            off = abs(df_year) if df_year is not None else 200
            arrival_yr[i] = -200 + round((j + 1) / (nuclear_n + 1) * min(200, off - 200))
        else:
            # El Dorado — beyond Σ2ⁿ budget
            claimed[i]    = None
            arrival_yr[i] = None

    # Recompute DF after redistribution
    df_year = df_arch = df_detector = None
    for i in range(N):
        if claimed[i] is None:
            continue
        for nb in adj[i]:
            if claimed[nb] is not None and claimed[nb] != claimed[i]:
                yr = max(arrival_yr[i] or 0, arrival_yr[nb] or 0)
                if df_year is None or yr < df_year:
                    df_year = yr; df_arch = i; df_detector = claimed[i]

    # Null out post-DF claims
    for i in range(N):
        if arrival_yr[i] is not None and df_year is not None and arrival_yr[i] > df_year:
            claimed[i]    = None
            arrival_yr[i] = None

    return claimed, arrival_yr, df_year, df_arch, df_detector


