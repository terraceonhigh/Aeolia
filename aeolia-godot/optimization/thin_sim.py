# ---------------------------------------------------------------------------
# Main simulation entry point
# ---------------------------------------------------------------------------

def simulate(base_world: dict,
             params: Optional[dict] = None,
             rng_seed: Optional[int] = None) -> dict:
    """
    Run the full parameterised simulation pipeline.

    Parameters
    ----------
    base_world : dict
        Output of run_headless.gd. Required keys:
            n, reach_arch, lattice_arch,
            archs (list of {lat, size, shelf_r, peak_count, potential}),
            adj  (adjacency list — list of lists of ints),
            substrate (list of {primary_crop} per arch),
            names (list of str)
        Optional: seed

    params : dict | None
        Parameter overrides. Any key absent falls back to DEFAULT_PARAMS.

    rng_seed : int | None
        RNG seed for stochastic elements. Defaults to base_world["seed"].
        Use different seeds to average over stochastic variance.

    Returns
    -------
    dict with keys:
        states        list[dict]  — per-arch final state
        df_year       int | None
        df_arch       int | None
        df_detector   str | None
        epi_log       list[dict]  — structured epidemic records for epi_component()
        reach_arch    int
        lattice_arch  int
        n             int
        archs         list[dict]
        substrate     list[dict]
        names         list[str]
        seed          int
    """
    p = {**DEFAULT_PARAMS, **(params or {})}
    N            = base_world["n"]
    reach_arch   = base_world["reach_arch"]
    lattice_arch = base_world["lattice_arch"]
    archs        = base_world["archs"]
    adj          = base_world["adj"]
    substrate    = base_world.get("substrate", [{}] * N)
    names        = base_world.get("names", [f"Arch {i}" for i in range(N)])
    seed         = rng_seed if rng_seed is not None else base_world.get("seed", 42)

    rng = random.Random(seed)

    def rnd() -> float:
        return rng.random()

    potential = [a["potential"] for a in archs]

    # ── Phase 1: Dijkstra ────────────────────────────────────────────────
    claimed, arrival_yr, hop_count, df_year, df_arch, df_detector = \
        _dijkstra(N, adj, reach_arch, lattice_arch)

    # ── Phase 2: Σ2ⁿ redistribution ─────────────────────────────────────
    claimed, arrival_yr, df_year, df_arch, df_detector = \
        _redistribute(N, claimed, arrival_yr, reach_arch, lattice_arch,
                      df_year, adj)

    # ── Phase 3: Status assignment ───────────────────────────────────────
    sovereign  = [-1] * N   # index of sovereign arch, -1 = none
    colony_yr  = [None] * N
    status_data = [
        {"sovereignty": 1.0, "tradeIntegration": 0.0,
         "status": "uncontacted", "eraOfContact": None}
        for _ in range(N)
    ]

    for i in range(N):
        if i == reach_arch or i == lattice_arch:
            status_data[i] = {
                "sovereignty": 1.0, "tradeIntegration": 1.0,
                "status": "core", "eraOfContact": None,
            }
            continue

        yr    = arrival_yr[i]
        power = claimed[i]
        hops  = hop_count[i]
        if yr is None or power is None:
            continue

        era = ("sail"       if yr < -2000 else
               "colonial"   if yr < -500  else
               "industrial" if yr < -200  else "nuclear")
        status_data[i]["eraOfContact"] = era

        if power == "reach":
            if hops <= p["reach_colony_hop_limit"] and era != "nuclear":
                sovereign[i] = reach_arch
                colony_yr[i] = yr + int(100 + rnd() * 300)
                status_data[i] = {
                    "sovereignty":       p["reach_colony_sovereignty"],
                    "tradeIntegration":  p["reach_colony_trade"],
                    "status": "colony",  "eraOfContact": era,
                }
            elif hops <= p["reach_client_hop_limit"] and era != "nuclear":
                status_data[i] = {
                    "sovereignty":      p["reach_client_sovereignty"],
                    "tradeIntegration": p["reach_client_trade"],
                    "status": "client", "eraOfContact": era,
                }
            else:
                status_data[i] = {
                    "sovereignty": 0.90, "tradeIntegration": 0.20,
                    "status": "contacted", "eraOfContact": era,
                }
        else:  # lattice
            if hops <= p["lattice_garrison_hop_limit"]:
                sovereign[i] = lattice_arch
                colony_yr[i] = yr + 200
                status_data[i] = {
                    "sovereignty":      p["lattice_garrison_sovereignty"],
                    "tradeIntegration": p["lattice_garrison_trade"],
                    "status": "garrison", "eraOfContact": era,
                }
            elif hops <= p["lattice_tributary_hop_limit"]:
                status_data[i] = {
                    "sovereignty":      p["lattice_tributary_sovereignty"],
                    "tradeIntegration": p["lattice_tributary_trade"],
                    "status": "tributary", "eraOfContact": era,
                }
            else:
                status_data[i] = {
                    "sovereignty": 0.90, "tradeIntegration": 0.15,
                    "status": "pulse", "eraOfContact": era,
                }

    # ── Phase 4: Population model ─────────────────────────────────────────

    pop  = [float(a["peak_count"]) * a["size"] * (3.0 + rnd() * 4.0) for a in archs]
    tech = [0.0] * N

    epi_log: list[dict] = []  # structured epidemic records for epi_component()

    # ── ERA 1: ANTIQUITY ─────────────────────────────────────────────────
    rate    = p["antiquity_growth_rate"]
    periods = p["antiquity_periods"]
    for i in range(N):
        pop[i]  *= (1.0 + rate * potential[i]) ** periods
        tech[i]  = potential[i] * (p["tech_init_low"] + rnd() * 1.5)

    tech[reach_arch]   = max(tech[reach_arch],   p["reach_tech_floor"])
    tech[lattice_arch] = max(tech[lattice_arch],  p["lattice_tech_floor"])
    pop[lattice_arch] *= p["lattice_antiquity_pop_mult"]

    # ── ERA 2: SERIAL CONTACT ─────────────────────────────────────────────
    reach_network   = 0
    lattice_network = 0

    for i in range(N):
        if i in (reach_arch, lattice_arch):
            continue
        yr    = arrival_yr[i]
        power = claimed[i]
        if yr is not None and -5000 <= yr < -2000:
            c_crop = "emmer" if power == "reach" else "paddi"
            t_crop = substrate[i].get("primary_crop", "emmer") if i < len(substrate) else "emmer"
            dist   = _crop_distance(c_crop, t_crop)
            base_s = p["shock_base_min"] + rnd() * p["shock_base_range"]
            shock  = 1.0 - base_s * dist
            pop[i] *= shock
            epi_log.append({
                "arch": i, "era": "sail",
                "contactor_crop": c_crop, "contacted_crop": t_crop,
                "crop_distance": dist,
                "mortality_rate": 1.0 - shock,
            })
            trade_years = max(0, -2000 - yr)
            pop[i] *= 1.0 + float(trade_years) * p["trade_recovery_rate"]
            tech[i] += 0.5 + rnd() * 0.5
            if power == "reach":
                reach_network   += 1
            else:
                lattice_network += 1
        elif yr is None or yr >= -2000:
            pop[i] *= 1.0 + 0.001 * potential[i] * 30.0

    # Reach: higher log₂ coefficient (A₀=1.2, δ=0.08)
    pop[reach_arch] *= (
        p["reach_serial_pop_base"] *
        (1.0 + _log2(1.0 + reach_network) * p["reach_serial_log_coeff"])
    )
    tech[reach_arch] = min(p["serial_tech_ceiling"],
                           tech[reach_arch] + p["reach_serial_tech_bonus"])

    # Lattice: larger base (paddi surplus), lower log₂ coefficient
    pop[lattice_arch] *= (
        p["lattice_serial_pop_base"] *
        (1.0 + _log2(1.0 + lattice_network) * p["lattice_serial_log_coeff"])
    )
    tech[lattice_arch] = min(p["serial_tech_ceiling"],
                             tech[lattice_arch] + p["lattice_serial_tech_bonus"])

    # ── ERA 3: COLONIAL EMPIRES ───────────────────────────────────────────
    reach_colonies  = 0
    lattice_garrisons = 0
    lattice_tribs   = 0
    total_extracted = 0.0
    total_enslaved  = 0.0
    extraction_rate_for = [0.0] * N

    for i in range(N):
        if i in (reach_arch, lattice_arch):
            continue
        yr    = arrival_yr[i]
        power = claimed[i]
        sd    = status_data[i]
        if yr is None:
            continue

        if yr < -500:
            if sd["status"] == "colony":
                col_start = colony_yr[i] if colony_yr[i] is not None else yr
                col_years = max(0, -500 - col_start)
                extr = min(0.30,
                           p["extraction_base_rate"] +
                           col_years * p["extraction_growth_per_year"])
                extraction_rate_for[i] = extr
                extracted = pop[i] * extr
                pop[i]   -= extracted
                total_extracted += extracted
                enslaved  = pop[i] * (0.05 + rnd() * 0.10)
                pop[i]   -= enslaved
                total_enslaved += enslaved
                pop[i]   += (p["settler_immigration_min"] +
                              rnd() * p["settler_immigration_range"])
                reach_colonies += 1

            elif sd["status"] == "garrison":
                abs_r    = p["garrison_absorption_min"] + rnd() * p["garrison_absorption_range"]
                absorbed = pop[i] * abs_r
                pop[i]  -= absorbed
                pop[lattice_arch] += absorbed
                lattice_garrisons += 1

            elif sd["status"] == "tributary":
                trib_r  = p["tributary_tribute_min"] + rnd() * p["tributary_tribute_range"]
                tribute = pop[i] * trib_r
                pop[lattice_arch] += tribute
                lattice_tribs += 1

            elif sd["status"] == "client":
                pop[i]  *= 1.0 + _log2(1.0 + reach_network) * 0.10
                tech[i] += 0.3 + rnd() * 0.3

        if yr is not None and -2000 <= yr < -500:
            c_crop = "emmer" if power == "reach" else "paddi"
            t_crop = substrate[i].get("primary_crop", "emmer") if i < len(substrate) else "emmer"
            dist   = _crop_distance(c_crop, t_crop)
            base_s = p["shock_base_min"] + rnd() * p["shock_base_range"]
            shock  = 1.0 - base_s * dist
            pop[i] *= shock
            epi_log.append({
                "arch": i, "era": "colonial",
                "contactor_crop": c_crop, "contacted_crop": t_crop,
                "crop_distance": dist,
                "mortality_rate": 1.0 - shock,
            })
            tech[i] += 0.3
            if power == "reach":
                reach_network   += 1
            elif power == "lattice":
                lattice_network += 1

    pop[reach_arch]   *= 1.0 + reach_colonies * p["reach_colonies_pop_coeff"]
    pop[reach_arch]   += total_extracted * p["extracted_transit_eff"] + total_enslaved
    lattice_integrated = lattice_garrisons + lattice_tribs
    pop[lattice_arch] *= (1.0
                          + lattice_garrisons * p["lattice_garrisons_pop_coeff"]
                          + lattice_tribs     * p["lattice_tribs_pop_coeff"])

    for i in range(N):
        if claimed[i] is not None or i in (reach_arch, lattice_arch):
            continue
        pop[i]  *= 1.0 + 0.0005 * potential[i] * 15.0
        tech[i]  = min(4.0, tech[i] + potential[i] * 0.1)

    # ── ERA 4: INDUSTRIAL ─────────────────────────────────────────────────
    total_network = reach_network + lattice_network

    for i in range(N):
        if i == reach_arch:
            pop[i] *= ((1.0 + tech[i] * p["reach_ind_tech_coeff"]
                             + potential[i] * p["reach_ind_resource_coeff"]) *
                        (1.0 + _log2(1.0 + total_network) * p["reach_ind_log_coeff"]))
            tech[i] = min(8.0, tech[i] + potential[i] * p["reach_ind_tech_growth"])
            continue

        if i == lattice_arch:
            pop[i] *= ((1.0 + tech[i] * p["lattice_ind_tech_coeff"]
                             + potential[i] * p["lattice_ind_resource_coeff"]) *
                        (1.0 + _log2(1.0 + lattice_integrated) * p["lattice_ind_log_coeff"]))
            tech[i] = min(8.0, tech[i] + potential[i] * p["lattice_ind_tech_growth"])
            continue

        if claimed[i] is None:
            pop[i]  *= 1.0 + 0.0003 * potential[i] * 6.0
            tech[i]  = min(4.5, tech[i] + potential[i] * 0.05)
            continue

        if sovereign[i] >= 0:  # colony / garrison
            pop[i]  *= (1.0 + tech[i] * p["colony_tech_coeff"]
                             + potential[i] * p["colony_resource_coeff"])
            tech[i]  = min(6.0, tech[i] + potential[i] * p["colony_tech_growth"])
        else:
            pop[i]  *= (1.0 + tech[i] * p["free_tech_coeff"]
                             + potential[i] * p["free_resource_coeff"])
            tech[i]  = min(7.5, tech[i] + potential[i] * p["free_tech_growth"])

        yr = arrival_yr[i]
        if yr is not None and -500 <= yr < -200:
            pop[i] *= 0.5 + rnd() * 0.3

    tech[reach_arch]   = max(tech[reach_arch],   p["reach_ind_tech_floor"])
    tech[lattice_arch] = max(tech[lattice_arch],  p["lattice_ind_tech_floor"])

    # ── ERA 4→5 TRANSITION: sovereignty drift ────────────────────────────
    for i in range(N):
        if claimed[i] is None or i in (reach_arch, lattice_arch):
            continue
        sd = status_data[i]
        if sd["status"] == "colony":
            sd["sovereignty"] = min(0.45,
                sd["sovereignty"] + extraction_rate_for[i] * p["colony_sov_drift_coeff"])
        elif sd["status"] == "client":
            age = max(0, -500 - (arrival_yr[i] or -500))
            sd["sovereignty"] = min(0.80,
                sd["sovereignty"] + float(age) * p["client_sov_drift_rate"])

    # ── ERA 5: NUCLEAR ────────────────────────────────────────────────────
    pop[reach_arch]   *= p["reach_nuclear_pop_mult"]
    tech[reach_arch]   = 10.0              # fixed to match lore end-state
    pop[lattice_arch] *= p["lattice_nuclear_pop_mult"]
    tech[lattice_arch] = 9.5

    for i in range(N):
        if i in (reach_arch, lattice_arch) or claimed[i] is None:
            continue

        if sovereign[i] == reach_arch:
            access = p["nuclear_access_colony"]
        elif sovereign[i] == lattice_arch:
            access = p["nuclear_access_garrison"]
        else:
            access = p["nuclear_access_contact"]

        if sovereign[i] >= 0 and rnd() < p["green_revolution_prob"]:
            pop[i] *= (p["green_revolution_mult_min"] +
                       rnd() * p["green_revolution_mult_range"])

        pop[i]  *= 1.0 + access * p["nuclear_pop_coeff"]
        tech[i]  = min(10.0, tech[i] + access)

    # ── Post-nuclear sovereignty recovery ─────────────────────────────────
    for i in range(N):
        if claimed[i] is None or i in (reach_arch, lattice_arch):
            continue
        sd = status_data[i]
        if sd["status"] == "colony":
            sd["sovereignty"]      = min(0.75,
                sd["sovereignty"] + p["colony_nuclear_sov_recovery"])
            sd["tradeIntegration"] *= p["colony_nuclear_trade_decay"]
        elif sd["status"] == "garrison":
            sd["sovereignty"]      = min(0.65,
                sd["sovereignty"] + p["garrison_nuclear_sov_recovery"])

    # ── Phase 5: Final states ─────────────────────────────────────────────
    max_pop = max(pop) if pop else 1.0

    states: list[dict] = []
    for i in range(N):
        sd = status_data[i]
        if i == reach_arch:
            faction = "reach"
        elif i == lattice_arch:
            faction = "lattice"
        elif claimed[i] is None:
            faction = "unknown"
        else:
            faction = claimed[i]

        states.append({
            "faction":          faction,
            "status":           sd["status"],
            "name":             names[i] if i < len(names) else f"Arch {i}",
            "population":       round(pop[i]),
            "tech":             round(tech[i] * 10) / 10.0,
            "sovereignty":      sd["sovereignty"],
            "tradeIntegration": sd["tradeIntegration"],
            "eraOfContact":     sd["eraOfContact"],
            "hopCount":         hop_count[i],
            "urbanization":     pop[i] / max_pop,
        })

    return {
        "states":       states,
        "df_year":      df_year,
        "df_arch":      df_arch,
        "df_detector":  df_detector,
        "epi_log":      epi_log,
        "reach_arch":   reach_arch,
        "lattice_arch": lattice_arch,
        "n":            N,
        "archs":        archs,
        "substrate":    substrate,
        "names":        names,
        "seed":         seed,
    }


